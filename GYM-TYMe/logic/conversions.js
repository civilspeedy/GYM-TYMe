/**
 * Kilogram to pounds conversion
 * @param {number} kg kilogram value
 * @returns {number} the value in pounds
 */
const kgToPounds = (kg) => kg * 2.2;

/**
 * Pounds to kilogram conversion
 * @param {number} pounds pounds value
 * @returns {number} the value in kilograms
 */
const poundsToKg = (pounds) => pounds / 2.205;

/**
 * Kilometers to miles conversion
 * @param {number} km kilometer value
 * @returns {number} the value in miles
 */
const kmToMiles = (km) => km * 0.621371;

/**
 * Miles to kilometers conversion
 * @param {number} miles mile value
 * @returns {number} value in kilometers
 */
const milesToKm = (miles) => miles * 1.60934;
